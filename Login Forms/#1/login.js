  const signUpButton = document.getElementById('signUp');
  const signInButton = document.getElementById('signIn');
  const container = document.getElementById('container');
  
  const languageSelect = document.getElementById('language-select');
  languageSelect.addEventListener('change', () => {
    const selectedLanguage = languageSelect.value;
    translateUI(selectedLanguage);
  });
  
  function translateUI(language) {
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
      const key = element.getAttribute('data-translate');
      if (translations[language] && translations[language][key]) {
        if (element.tagName === 'INPUT' && element.getAttribute('type') === 'text') {
          // Placeholder çevirisi için input alanını seçip placeholder'ı güncelle
          element.placeholder = translations[language][key];
        } else if (element.tagName === 'INPUT' && element.getAttribute('type') === 'email') {
          // Email input alanı için placeholder'ı güncelle
          element.placeholder = translations[language][key];
        } else if (element.tagName === 'INPUT' && element.getAttribute('type') === 'password') {
          // Password input alanı için placeholder'ı güncelle
          element.placeholder = translations[language][key];
        } else {
          element.textContent = translations[language][key];
        }
      }
    });
  }
  signUpButton.addEventListener('click', () => {
    container.classList.add("right-panel-active");
    translateUI(languageSelect.value);
  });
  
  signInButton.addEventListener('click', () => {
    container.classList.remove("right-panel-active");
    translateUI(languageSelect.value);
  });
  
  
